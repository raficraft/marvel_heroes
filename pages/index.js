import Head from "next/head";
import React, { useContext, useEffect } from "react";
import Header from "../engine/component/Header/Header";
import styles from "../styles/Home.module.scss";
import Card from "./../engine/component/Card/Card";
import { HeroesContext } from "./../engine/context/characProvider";

import Text_animate from "./../engine/component/TextAnimate/TextAnimate";
import {
  translateLeftToRight,
  translatleByBottom,
} from "../engine/component/TextAnimate/styledAnimation";
import useGetimages from "../engine/hooks/useGetImages";

export default function Home() {
  const { heroe, setHeroe } = useContext(HeroesContext);

  const [filesInfo, loading] = useGetimages([
    `heroes/${heroe.current.heroesID}/`,
  ]);

  const lastIndex = heroe.all.length - 1;

  const nextHeroes = () => {
    const nextID = heroe.all[heroe.current.id].id + 1;
    nextID > lastIndex ? (nextID = 0) : nextID;

    setHeroe((s) => ({
      ...s,
      current: heroe.all[nextID],
    }));
  };

  const prevHeroes = () => {
    const nextID = heroe.all[heroe.current.id].id - 1;
    nextID < 0 ? (nextID = lastIndex) : (nextID = nextID);

    setHeroe((s) => ({
      ...s,
      current: heroe.all[nextID],
    }));
  };

  return (
    <>
      <Head>
        <title>Marvel Heroes</title>
        <meta
          name="description"
          content="Marvel Heroes generated by create next app"
        />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className={`${styles.main} ${styles[heroe.current.heroesID]}`}>
        <Header />

        <section className={styles.identity}>
          <div className={styles.temp}>
            <div className={styles.content}>
              <Text_animate
                params={{
                  method: "letterByLetter",
                  shiftDelay: 80,
                  spacingDimension: "10px",
                }}
                key={heroe.current.id + "_title"}
                frames={translateLeftToRight}
                styles={{
                  display: "inline-block",
                  overflow: "hidden",
                  opacity: 0,
                  "animation-fill-mode": "forwards",
                  "animation-duration": "500ms",
                }}
              >
                <h1 className={styles.title}>{heroe.current.heroes_name}</h1>
              </Text_animate>

              <Text_animate
                params={{
                  method: "fullText",
                }}
                key={heroe.current.id}
                frames={translatleByBottom}
                styles={{
                  display: "inline-block",
                  overflow: "hidden",
                  "font-size": "16px",
                  "animation-fill-mode": "forwards",
                  "animation-duration": "500ms",
                  "line-height": "1.5rem",
                  "text-indent": "1rem",
                }}
              >
                <p className={styles.whiteText}>{heroe.current.desc}</p>
              </Text_animate>
            </div>
          </div>
          {!loading && <Card heroe={heroe} img={filesInfo[0]} />}
        </section>
      </main>
      <span
        className={`${styles.button_container} ${styles.button_containerPrev}`}
      >
        <button
          className={`${styles.button} ${styles.buttonPrev}`}
          onClick={prevHeroes}
          data-hsprev
        ></button>
      </span>

      <span
        className={`${styles.button_container} ${styles.button_containerNext}`}
      >
        <button
          className={`${styles.button} ${styles.buttonNext}`}
          onClick={nextHeroes}
          data-hsnext
        ></button>
      </span>
    </>
  );
}
